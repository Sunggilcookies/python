import turtle as t

t.shape('turtle')
for i in range(4):
    t.forward(100)
    t.left(90)

for i in range(3):
    t.left(120)
    t.forward(100)
t.color('blue')
t.shapesize(3)

for i in range(3):
    t.right(120)
    t.forward(100)
for i in range(4):

    t.forward(100)
    t.right(90)


t.mainloop()
